/// Return the glyph for a character (5×7 bitmap, MSB = left).
/// Falls back to space for unmapped characters.
pub fn glyph(ch: char) -> &'static Glyph {
    match ch {
        ' '  => &SP,
        '!'  => &EX,
        '"'  => &QT,
        '#'  => &HS,
        '$'  => &DL,
        '%'  => &PC,
        '&'  => &AM,
        '\'' => &AP,
        '('  => &LP,
        ')'  => &RP,
        '*'  => &AS,
        '+'  => &PL,
        ','  => &CM,
        '-'  => &MI,
        '.'  => &DT,
        '/'  => &SL,
        '0'  => &G0,
        '1'  => &G1,
        '2'  => &G2,
        '3'  => &G3,
        '4'  => &G4,
        '5'  => &G5,
        '6'  => &G6,
        '7'  => &G7,
        '8'  => &G8,
        '9'  => &G9,
        ':'  => &CO,
        ';'  => &SC,
        '<'  => &LT,
        '='  => &EQ,
        '>'  => &GT,
        '?'  => &QM,
        '@'  => &AT,
        'A'  => &GA,
        'B'  => &GB,
        'C'  => &GC,
        'D'  => &GD,
        'E'  => &GE,
        'F'  => &GF,
        'G'  => &GG,
        'H'  => &GH,
        'I'  => &GI,
        'J'  => &GJ,
        'K'  => &GK,
        'L'  => &GL,
        'M'  => &GM,
        'N'  => &GN,
        'O'  => &GO,
        'P'  => &GP,
        'Q'  => &GQ,
        'R'  => &GR,
        'S'  => &GS,
        'T'  => &GT_,
        'U'  => &GU,
        'V'  => &GV,
        'W'  => &GW,
        'X'  => &GX,
        'Y'  => &GY,
        'Z'  => &GZ,
        '['  => &LB,
        '\\' => &BS,
        ']'  => &RB,
        '^'  => &CR,
        '_'  => &UN,
        '`'  => &BQ,
        'a'  => &LC,
        'b'  => &LB_,
        'c'  => &LC_,
        'd'  => &LD,
        'e'  => &LE,
        'f'  => &LF,
        'g'  => &LG,
        'h'  => &LH,
        'i'  => &LI,
        'j'  => &LJ,
        'k'  => &LK,
        'l'  => &LL,
        'm'  => &LM,
        'n'  => &LN,
        'o'  => &LO,
        'p'  => &LP_,
        'q'  => &LQ,
        'r'  => &LR,
        's'  => &LS,
        't'  => &LT__,
        'u'  => &LU,
        'v'  => &LV,
        'w'  => &LW,
        'x'  => &LX,
        'y'  => &LY,
        'z'  => &LZ,
        '{'  => &LBC,
        '|'  => &PI,
        '}'  => &RBC,
        '~'  => &TL,
        '\u{00B0}' => &DEGREE, // °
        _    => &SP,
    }
}

