// 5×7 pixel bitmap font.
// Each character is 7 rows × 5 columns; rows are stored as u8 bitmasks,
// MSB = leftmost pixel.  Printable ASCII 0x20–0x7E plus a few extras.

/// Glyph data: 7 rows, each row is a 5-bit mask stored in the high bits of a u8.
